    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $user = User::find($id);
        if ($user) {
            return view('admin.user.edit', compact(['user']));
        } else {
            return back()->with('error', 'هذه البيانات غير موجودة');
        }
    }

    /**
